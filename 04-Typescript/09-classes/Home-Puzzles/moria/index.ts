
class celebrity {
    name: string;
    followers: number;
    genre: string;
    account: string;
    Instagram: string;


    constructor(
        name: string,
        followers: number,
        genre: string,
        account: string,
        Instagram: string,

    ) {
        this.name = name;
        this.followers = followers;
        this.genre = genre;
        this.account = account;
        this.Instagram = Instagram;
    }
}